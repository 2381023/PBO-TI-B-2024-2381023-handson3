package TugasKelas;

public class Tugas4While {
    public static void main(String[] args) {
        System.out.println("angka genap dari 1 sampai 20 adalah: ");
        int i = 1;
        while (i <= 20) {
            if (i % 2 == 0){
                System.out.println(i + " ");
            }
            i++ ;
        }

    }
}
